def get_moments_for_servant(name_cn, class_name=None, traits=None):
    if name_cn in SERVANT_MOMENTS_CN:
        return SERVANT_MOMENTS_CN[name_cn]

    moments = []
    if class_name and class_name in CLASS_TEMPLATES:
        moments.extend(CLASS_TEMPLATES[class_name])

    if traits:
        for trait in traits:
            if trait in TRAIT_TEMPLATES:
                moments.extend(TRAIT_TEMPLATES[trait])

    if not moments:
        moments = list(GENERIC_TEMPLATES)

    return moments


def load_all_servant_data():
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    c.execute('SELECT name, class, rarity, traits, nicknames FROM servants')
    servants = {}
    for row in c.fetchall():
        name = row[0]
        traits = json.loads(row[3]) if row[3] else []
        servants[name] = {
            'class': row[1],
            'rarity': row[2],
            'traits': traits,
            'nicknames': row[4] or '',
        }
    conn.close()
    return servants


def build_moments_json():
    servants = load_all_servant_data()
    result = {}
    for name, info in servants.items():
        moments = get_moments_for_servant(name, info['class'], info['traits'])
        result[name] = moments
    return result
